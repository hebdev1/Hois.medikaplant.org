/**
 * Generates lib/site-image-slots.ts from the image URLs currently hard-coded
 * in the homepage components. Those URLs become the FALLBACKS; admins
 * override them per slot from /admin/imaj (table public.site_images).
 *
 *   node scripts/gen-site-image-slots.mjs
 *
 * Re-run only if you add a new image slot to a component.
 */
import fs from 'node:fs';

const URL_RE = /https:\/\/images\.(?:unsplash|pexels)\.com[^\s"'`)]*/g;

function urls(file) {
  return [...fs.readFileSync(file, 'utf8').matchAll(URL_RE)].map((m) => m[0]);
}

// hero-section builds its URLs as `${base}${PX}` — re-attach the suffix.
const heroSrc = fs.readFileSync('components/ui/hero-section.tsx', 'utf8');
const PX = (heroSrc.match(/const PX = '([^']+)'/) || [, ''])[1];

const groups = [
  {
    section: 'Ewo (an wo paj la)',
    prefix: 'hero',
    list: urls('components/ui/hero-section.tsx').map((u) => u + PX),
  },
  {
    section: 'Karousèl',
    prefix: 'carousel',
    list: urls('components/ui/feature-carousel.tsx'),
  },
  {
    section: 'Temwayaj',
    prefix: 'testimonial',
    list: urls('components/ui/testimonials-section.tsx'),
  },
  {
    section: 'Apwopo nou',
    prefix: 'about',
    list: urls('components/ui/about-section.tsx'),
  },
];

const slots = [];
for (const g of groups) {
  g.list.forEach((url, i) => {
    slots.push({
      key: `${g.prefix}-${i + 1}`,
      section: g.section,
      label: `${g.section} — imaj ${i + 1}`,
      // Sources are scraped as raw text, so a URL built with a template
      // literal (e.g. `...jpeg${PX}`) carries the placeholder along. Strip it
      // — the resolved query string is already in the captured text.
      fallback: url.replace(/\$\{[A-Za-z_$][\w$]*\}/g, ''),
    });
  });
}

const out =
  `// AUTO-GENERATED by scripts/gen-site-image-slots.mjs — do not edit by hand.\n` +
  `// Each slot's \`fallback\` is the image originally shipped in the component.\n` +
  `// Admins override any slot from /admin/imaj; overrides live in\n` +
  `// public.site_images and win over these defaults.\n\n` +
  `export type SiteImageSlot = {\n` +
  `  key: string;\n  section: string;\n  label: string;\n  fallback: string;\n};\n\n` +
  `export const SITE_IMAGE_SLOTS: readonly SiteImageSlot[] = [\n` +
  slots
    .map(
      (s) =>
        `  { key: '${s.key}', section: '${s.section}', label: '${s.label}', fallback: '${s.fallback}' },`
    )
    .join('\n') +
  `\n];\n`;

fs.writeFileSync('lib/site-image-slots.ts', out, 'utf8');
console.log(`${slots.length} slots →  lib/site-image-slots.ts`);
for (const g of groups) console.log(`  ${String(g.list.length).padStart(2)}  ${g.section}`);
